package d_Polimorfismo;

/**
 * Clase Vehiculo que implementa métodos y atributos
 */
public class Vehiculo {
int speed;

/**
 * Incrementa velocidad de Vehículo
 */
public void speedUp() {
     speed += 10;
 }

/**
 * Devuelve velocidad del Vehiculo
 */
 public int getSpeed() {
     return speed;
 }
}

